package uk.ewancroft.atpkt.gen

import com.squareup.kotlinpoet.*
import uk.ewancroft.atpkt.lexicon.*
import java.io.File

// ── Full lexicon code generator ────────────────────

/**
 * Extended LexiconGenerator capable of producing the full API surface area.
 * Still a work-in-progress: the namespace layer and model generation methods
 * are stubs awaiting the full KotlinPoet wiring.
 */
import com.squareup.kotlinpoet.*
import com.squareup.kotlinpoet.ParameterizedTypeName.Companion.parameterizedBy
import uk.ewancroft.atpkt.client.XrpcClient
import uk.ewancroft.atpkt.lexicon.LexiconSchema
import uk.ewancroft.atpkt.lexicon.LexiconDefinition
import java.io.File as JavaFile

class FullLexiconGenerator(private val outputDir: JavaFile) {

    fun generateAll(schemas: List<LexiconSchema>) {
        schemas.forEach { schema ->
            generateNamespaceLayer(schema)
            generateModels(schema)
        }
    }

    private fun generateNamespaceLayer(schema: LexiconSchema) {
        val namespace = schema.id.substringBeforeLast(".")
        val className = namespace.split(".").last().replaceFirstChar { it.uppercase() } + "NS"
        val packageName = "uk.ewancroft.atpkt.client"
        val fileBuilder = FileSpec.builder(packageName, className)
            .addFileComment("Automatically generated by atpkt FullLexiconGenerator. DO NOT EDIT.")

        val classBuilder = TypeSpec.classBuilder(className)
            .addModifiers(KModifier.OPEN)

        schema.defs.values.forEach { def ->
            when (def.type) {
                "query" -> {
                    val methodName = schema.id.substringAfterLast(".")
                    val outputType = def.output?.schema?.type?.replaceFirstChar { it.uppercase() } ?: "Unit"
                    val paramsType = def.parameters?.properties?.get("type")?.type?.replaceFirstChar { it.uppercase() } ?: "Unit"

                    val methodSpec = FunSpec.builder(methodName)
                        .addModifiers(KModifier.SUSPEND, KModifier.OPEN)
                        .returns(Result::class.asClassName().parameterizedBy(
                            ClassName("uk.ewancroft.atpkt.generated.${namespace}", outputType)
                        ))
                        .addParameter("client", XrpcClient::class)
                        .addParameter("params", ClassName("uk.ewancroft.atpkt.generated.${namespace}", paramsType))
                        .addCode("""
                            |return client.executeQuery("${schema.id}", params)
                            |""".trimMargin())
                        .build()
                    classBuilder.addFunction(methodSpec)
                }
                "procedure" -> {
                    val methodName = schema.id.substringAfterLast(".")
                    val outputType = def.output?.schema?.type?.replaceFirstChar { it.uppercase() } ?: "Unit"
                    val inputType = def.input?.schema?.type?.replaceFirstChar { it.uppercase() } ?: "Unit"

                    val methodSpec = FunSpec.builder(methodName)
                        .addModifiers(KModifier.SUSPEND, KModifier.OPEN)
                        .returns(Result::class.asClassName().parameterizedBy(
                            ClassName("uk.ewancroft.atpkt.generated.${namespace}", outputType)
                        ))
                        .addParameter("client", XrpcClient::class)
                        .addParameter("input", ClassName("uk.ewancroft.atpkt.generated.${namespace}", inputType))
                        .addCode("""
                            |return client.executeProcedure("${schema.id}", input)
                            |""".trimMargin())
                        .build()
                    classBuilder.addFunction(methodSpec)
                }
                else -> Unit
            }
        }

        fileBuilder.addType(classBuilder.build())
        fileBuilder.build().writeTo(outputDir.toPath())
    }

    private fun generateModels(schema: LexiconSchema) {
        LexiconGenerator.generateKotlinClass(schema.id, outputDir)
    }
}
