package uk.ewancroft.atpkt.gen

import com.squareup.kotlinpoet.BOOLEAN
import com.squareup.kotlinpoet.ClassName
import com.squareup.kotlinpoet.FileSpec
import com.squareup.kotlinpoet.FunSpec
import com.squareup.kotlinpoet.KModifier
import com.squareup.kotlinpoet.LONG
import com.squareup.kotlinpoet.ParameterSpec
import com.squareup.kotlinpoet.PropertySpec
import com.squareup.kotlinpoet.STRING
import com.squareup.kotlinpoet.TypeSpec
import com.squareup.kotlinpoet.ParameterizedTypeName.Companion.parameterizedBy
import kotlinx.serialization.json.JsonObject
import kotlinx.serialization.json.JsonPrimitive
import kotlinx.serialization.json.jsonArray
import kotlinx.serialization.json.jsonObject
import kotlinx.serialization.json.jsonPrimitive
import uk.ewancroft.atpkt.lexicon.LexiconSchema
import uk.ewancroft.atpkt.lexicon.registry.LexiconRegistry
import java.io.File

// ── Lexicon-to-Kotlin code generator ───────────────

/**
 * Minimal lexicon generator used by the test suite and CLI.
 *
 * It reads raw lexicon JSON from [LexiconRegistry] and emits Kotlin data
 * classes for record/object definitions plus sealed union markers for union
 * properties.
 */
class LexiconGenerator(private val outputDir: File) {
    fun generate(schema: LexiconSchema) {
        generateKotlinClass(schema.id, outputDir)
    }

    companion object {
        private val jsonSerializable = ClassName("kotlinx.serialization", "Serializable")

        @JvmStatic
        fun generateKotlinClass(id: String, outputDir: File = File("src/main/kotlin")): FileSpec? {
            val schemaJson = LexiconRegistry.get(id) ?: return null
            val defs = schemaJson["defs"]?.jsonObject ?: return null

            val packageName = "uk.ewancroft.atpkt.generated.${id.substringBeforeLast(".")}"
            val fileName = id.substringAfterLast(".").replaceFirstChar { it.uppercase() }
            val fileBuilder = FileSpec.builder(packageName, fileName)
                .addFileComment("Automatically generated by atpkt LexiconGenerator. DO NOT EDIT.")

            val generatedTypes = mutableListOf<TypeSpec>()
            val unionNames = linkedSetOf<String>()

            defs.forEach { (defName, defElement) ->
                val defObj = defElement.jsonObject
                when (defObj["type"]?.jsonPrimitive?.content) {
                    "record", "object" -> {
                        val typeName = if (defName == "main") fileName else defName.replaceFirstChar { it.uppercase() }
                        val schemaForType = defObj["record"]?.jsonObject ?: defObj
                        generatedTypes += buildDataClass(typeName, schemaForType, packageName, unionNames)
                    }
                    else -> Unit
                }
            }

            unionNames.forEach { unionName ->
                generatedTypes += TypeSpec.interfaceBuilder(unionName)
                    .addAnnotation(jsonSerializable)
                    .addModifiers(KModifier.SEALED)
                    .build()
            }

            if (generatedTypes.isEmpty()) return null
            generatedTypes.forEach(fileBuilder::addType)
            val fileSpec = fileBuilder.build()
            fileSpec.writeTo(outputDir.toPath())
            return fileSpec
        }
    }
}

private fun buildDataClass(
    name: String,
    schema: JsonObject,
    packageName: String,
    unionNames: MutableSet<String>,
): TypeSpec {
    val builder = TypeSpec.classBuilder(name)
        .addAnnotation(ClassName("kotlinx.serialization", "Serializable"))
        .addModifiers(KModifier.DATA)

    val required = schema["required"]?.jsonArray?.mapNotNull { it.jsonPrimitive.contentOrNull() }?.toSet().orEmpty()
    val properties = schema["properties"]?.jsonObject ?: JsonObject(emptyMap())

    val constructorBuilder = FunSpec.constructorBuilder()
    properties.forEach { (propName, propElement) ->
        val propSchema = propElement.jsonObject
        val kotlinType = buildTypeForProperty(name, propName, propSchema, packageName, unionNames)
        val isNullable = required.isNotEmpty() && !required.contains(propName)
        val effectiveType = kotlinType.copy(nullable = isNullable)
        val parameter = ParameterSpec.builder(propName, effectiveType)
        if (isNullable) parameter.defaultValue("null")
        constructorBuilder.addParameter(parameter.build())
        builder.addProperty(
            PropertySpec.builder(propName, effectiveType)
                .initializer(propName)
                .build()
        )
    }

    return builder.primaryConstructor(constructorBuilder.build()).build()
}

private fun buildTypeForProperty(
    parentName: String,
    propName: String,
    schema: JsonObject,
    packageName: String,
    unionNames: MutableSet<String>,
): com.squareup.kotlinpoet.TypeName {
    schema["type"]?.jsonPrimitive?.contentOrNull()?.let { type ->
        when (type) {
            "string" -> return STRING
            "integer" -> return LONG
            "boolean" -> return BOOLEAN
            "unknown", "object", "bytes" -> return ClassName("kotlinx.serialization.json", "JsonElement")
            "array" -> {
                val itemType = schema["items"]?.jsonObject?.let {
                    buildTypeForProperty(parentName, "${propName}Item", it, packageName, unionNames)
                } ?: ClassName("kotlinx.serialization.json", "JsonElement")
                return com.squareup.kotlinpoet.LIST.parameterizedBy(itemType)
            }
            "union" -> {
                val unionName = "${parentName}${propName.replaceFirstChar { it.uppercase() }}Union"
                unionNames += unionName
                return ClassName(packageName, unionName)
            }
            "ref" -> {
                val ref = schema["ref"]?.jsonPrimitive?.contentOrNull().orEmpty()
                return if (ref.startsWith("#")) {
                    ClassName(packageName, ref.removePrefix("#").replaceFirstChar { it.uppercase() })
                } else {
                    STRING
                }
            }
        }
    }

    schema["ref"]?.jsonPrimitive?.contentOrNull()?.let { ref ->
        return if (ref.startsWith("#")) {
            ClassName(packageName, ref.removePrefix("#").replaceFirstChar { it.uppercase() })
        } else {
            STRING
        }
    }

    return ClassName("kotlinx.serialization.json", "JsonElement")
}

private fun JsonPrimitive.contentOrNull(): String? = try {
    content
} catch (_: Exception) {
    null
}
